    size_t Dump(const GrammarData::ParamSpec& p)
    {
        ParamSpec  pitem;
        pitem.sign           = p.Negated;
        pitem.transformation = p.Transformation;
        pitem.minrepeat      = p.MinimumRepeat;
        pitem.anyrepeat      = p.AnyRepetition;
        pitem.opcode         = p.Opcode;
        switch(p.Opcode)
        {
            case NumConstant:
            {
                pitem.index = Dump(p.ConstantValue);
                pitem.count = 0;
                break;
            }
            case NamedHolder:
            case ImmedHolder:
            case RestHolder:
            {
                pitem.index = p.Index;
                pitem.count = 0;
                break;
            }
            case SubFunction:
            {
                pitem.index = Dump(*p.Func);
                pitem.count = 0;
                break;
            }
            default:
            {
                size_t i, c;
                Dump(p.Params, i, c);
                pitem.index = i;
                pitem.count = c;
                break;
            }
        }
        plist.push_back(pitem);
        return plist.size()-1;
    }
